TableGaussPatterson::TableGaussPatterson() : max_levels(0), nodes(0), weights(0), weights_offsets(0) {
        max_levels = 9;
        weights_offsets = new int[max_levels];
        weights_offsets[0] = 0;
        for( int l=0; l<max_levels-1; l++ ){
                weights_offsets[l+1] = weights_offsets[l] + meta.getNumPoints( l, rule_gausspatterson );
        }

        loadNodes();
        loadWeights();
}

TableGaussPatterson::~TableGaussPatterson(){
        if ( nodes != 0 ){ delete[] nodes; nodes = 0; };
        if ( weights != 0 ){ delete[] weights; weights = 0; };
        if ( weights_offsets != 0 ){ delete[] weights_offsets; weights_offsets = 0; };
}

int TableGaussPatterson::getMaxLeve(){
        return 9; // sync this with max_levels
}

double* TableGaussPatterson::getNodes( int level ) const{
        int num_points = meta.getNumPoints( level, rule_gausspatterson );
        double* x = new double[ num_points ];
        std::copy( nodes, nodes + num_points, x );
        return x;
}

double TableGaussPatterson::getWeight( int level, int point ) const{
        return weights[weights_offsets[level] + point];
}

